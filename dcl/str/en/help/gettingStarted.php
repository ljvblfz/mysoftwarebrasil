<H2>Getting Started With DCL</H2>
<BR>
<H3>What is DCL?</H3>
Double Choco Latte is a system for managing, well...stuff.  It is written to be useful in tracking software issues, but is also configurable enough to track almost any activity and retain history.
<BR>
<H3>What features does DCL support?</H3>
DCL supports a number of features and is in active development.  A current feature set at the time of this writing includes:
<UL>
<LI>Basic security settings to restrict user activity.</LI>
<LI>E-Mail notifications for work order activity.</LI>
<LI>My DCL page reflecting user's status in the system.</LI>
<LI>Some statistical reports.</LI>
<LI>Projects to group work orders together.</LI>
<LI>File attachments for work orders and projects.</LI>
<LI>Ability for users to add watches for work orders, projects, and products.</LI>
</UL>
<BR>
<H3>What features <I>will</I> DCL support?</H3>
A number of features are planned, and among those are:
<UL>
<LI>More robust project management.</LI>
</UL>
